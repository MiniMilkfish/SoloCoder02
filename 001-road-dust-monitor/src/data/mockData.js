export const generateHourlyDustData = () => {
  const hours = []
  const values = []
  for (let i = 0; i < 24; i++) {
    hours.push(`${i.toString().padStart(2, '0')}:00`)
    values.push((Math.random() * 0.3 + 0.05).toFixed(3))
  }
  return { hours, values }
}

